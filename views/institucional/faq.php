                    administrativamente geridos pelo IEFAP, em qualquer região do Brasil.</li>
                  <li>A indicação de alunos será válida somente para alunos matriculados em um dos cursos 
                    ministrados pelas IEs nas dependências do IEFAP que estejam rigorosamente em dia com as 
