              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion">
                  <img src="<?php echo Url::templatePath(); ?>images/logo-mini.png" alt="IEFAP"> <span>O que é 
                  um Trabalho de Conclusão de Curso (TCC)?</span>
                </a>
              </h4>
            </div>
            <div class="panel-collapse collapse">
              <div class="panel-body">
                <p>É a proposição mais importante das atividades discentes dos Cursos de Especialização. No 
                  entanto, o acadêmico, ao final do Curso de Pós-Graduação Lato Sensu, deve elaborar um 
                  trabalho científico, que seja resultado de uma pesquisa individual, que demonstre que o 
                  mesmo é capaz de demarcar determinado tema e pesquisar sobre ele, demonstrando os 
                  conhecimentos adquiridos durante seu Curso.</p><p>
                O TCC significa Trabalho de Conclusão de Curso, refere-se a uma dissertação científica, do 
                cunho relativo à iniciação cientifica que os acadêmicos ao final do Curso devem elaborar, 
                descrevendo sequencialmente, os sucessivos componentes para a construção do texto cientifico.</p><p>
                Este trabalho, que é desenvolvido sob a orientação de um docente, é uma atividade preparatória 
                para as tarefas que farão parte do cotidiano de sua área de profissional. Neste sentido, o 
                Trabalho de Conclusão de Curso é um dos instrumentos avaliativos de conclusão dos Cursos de 
                Pós-Graduação. Do resultado depende a concessão do título de Especialista, normalizado pela 
                Associação Brasileira de Normas Técnicas (ABNT).</p><p>
                Os itens desenvolvidos a seguir foram baseados nas Normas da Associação Brasileira de Normas 
                Técnicas - ABNT, considerando ser este órgão responsável pela normalização técnica brasileira.</p><p>
                As normas elaboradas pela ABNT podem ser consultadas através do site da entidade 
                (http://www.abntdigital.com.br). As normas da ABNT a serem aplicadas nos Trabalhos de 
                Conclusão de Curso devem ser as normas dos anos correspondentes – 2002, 2003, 2004. 2005 e 
                2006, particularmente das seguintes NBR:</p><p>
                NBR 6023 - Referências bibliográficas (2002)<br />
                NBR 10520 – Citações (2002)<br />
                NBR 6024 - Numeração sucessivas (2003)<br />
                NBR 6027 – Sumário (2003)<br />
                NBR 6028 – Resumo (2003)<br />
                NBR 12225 – Títulos de lombada (2004)<br />
                NBR 14724 – Preparo e apresentação (2006)</p><p>
                Essas etapas são importantíssima no TCC da Pós-Graduação, os Trabalhos de Conclusão de Curso 
                visa verificar a maturidade do aluno com relação a determinado assunto, relacionado ao 
                conjunto teórico da área de conhecimento do curso em questão. O que se observa, atualmente, 
                é certa dificuldade por parte dos alunos em produzir um trabalho com ideias claras, 
                interessantes e bem apresentadas. Muitas vezes o trabalho de conclusão de curso apresenta-se 
                como uma colagem de citações e repetições de frases de autores consagrados.</p><p>
                Uma das causas desta dificuldade reside em ler, efetivamente, um texto, ou seja, compreender 
                seu significado, o sentido amplo das ideias, relacionar um artigo a outros, e assim por 
                diante. Também é muito comum que alunos tenham muito boas ideias, mas não consigam 
                apresentá-las de maneira clara e bem estruturada.</p><p>
                De acordo com a ABNT (2003), duas definições são apresentadas para o Artigo Científico, 
                quanto à análise do conteúdo são elas:</p><p>
                a) O Artigo Original - Utilizado para o relatório de experiência de pesquisa, estudo de 
                caso etc. Neste caso são abordados temas únicos, delimitados, em que se serve de um 
                raciocínio rigoroso e metodológico - de acordo com as diretrizes lógicas da pesquisa 
                científica - de forma interpretativa, argumentativa, dissertativa e apreciativa, aferem-se 
                os respectivos resultados e avalia-se o avanço que da pesquisa em relação ao crescimento 
                cientifico da área, o que exige ampla informação cultural e muita maturidade intelectual, 
                inclusive por necessitar também de um referencial teórico abalizado de sustentação da idéia 
                nova, original e inédita que comunica (GONÇALVES, 2004).<br />
                b) O Artigo de Revisão - Significa um estudo aprofundado sobre o determinado tema com o 
                propósito de estabelecer um debate entre os autores pesquisados e deles com o autor do 
                artigo, para a identificação das idéias, posições e posturas acadêmicas, bem como o estado 
                da arte, marco teórico ou quadro teórico, principalmente por meio de publicações periódicas 
                científicas e especializadas, objetivando identificar o grau de profundidade dos estudos 
                desenvolvidos até o mesmo sobre o assunto (NBR 6022, 2003). </p>
              </div>
