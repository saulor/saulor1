                manutenção de benefícios à ocorrência de incapacidade ou invalidez, comprovada por inspeção 
                médico-pericial.</p><p>
                
                O Regime Trabalhista, ao adotar as estratégias de proteção à saúde do trabalhador, institui mecanismos 
                de monitoração dos indivíduos, visando a evitar ou identificar precocemente os agravos à sua saúde, 
                quando produzidos ou desencadeados pelo exercício do trabalho. Ao estabelecer a obrigatoriedade na 
                realização dos exames pré-admissional, periódico e demissional do trabalhador, criou recursos 
                médico-periciais voltados a identificar o nexo de causalidade entre os danos sofridos e a ocupação que 
                desempenha.</p><p>
                
                Do mesmo modo, nos serviços prestados ao Estado, os servidores públicos civis e militares estão amparados 
                por dispositivos dos Regimes Jurídicos a que estão filiados, sendo-lhes assegurada a proteção a saúde. 
                Para tanto, as estratégias utilizadas no acompanhamento do funcionário público em nada devem diferir 
                daquelas adotadas no Regime Trabalhista.</p><p>
                
                No Regime Trabalhista, como no Regime Jurídico do Servidor Público, a concessão ou manutenção de licenças 
                remuneradas, em virtude de doença ou acidente que produz incapacidade ou invalidez, está condicionada à 
                realização da inspeção médico-pericial.</p>
              </div>
            </div>
