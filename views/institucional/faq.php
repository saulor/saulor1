                <a data-toggle="collapse" data-parent="#accordion">
                  <img src="<?php echo Url::templatePath(); ?>images/logo-mini.png" alt="IEFAP"> <span>O que 
                  é Junta Médica Oficial?</span>
                </a>
              </h4>
            </div>
            <div class="panel-collapse collapse">
              <div class="panel-body">
                <p>São dois ou mais médicos, geralmente três, investidos em função pericial, mediante 
                  designação formal. A junta médica oficial poderá ser designada pela autoridade 
                  administrativa do órgão a que estiver vinculada a pessoa a ser periciada, o que ocorre na 
                  Administração Pública, ou pode ser nomeada pelo juiz, quando entender que o parecer 
                  médico-pericial subsidiará seu julgamento. Outrossim, esse recurso pode ser utilizado 
                  para atender diligências do Ministério Público, entre outros de ocorrência menos 
                  frequente.</p><p>
                
                A junta médica oficial recebe missão especifica, visando a definir o nexo de causalidade 
                objeto do julgamento, em nível judicial ou administrativo.</p><p>
                
                A junta deve reunir-se formalmente, em local, data e horário previamente estabelecidos, 
                realizar o exame com a presença de todos os seus integrantes, inclusive dos assistentes 
                técnicos (somente médicos), quando indicados pelas partes.</p><p>
                
                O laudo ou relatório médico e a conclusão médico-pericial devem ser datados e assinados 
                pela junta e pelos assistentes técnicos. Quando houver divergência na conclusão, os 
                pareceres discordantes serão apresentados em separado.</p><p>
                
                A junta médica poderá recorrer a exames subsidiários, pareceres de outros especialistas, 
                informações contidas em prontuário médico, sempre buscando melhor consistência em sua 
                conclusão.</p><p>
                
