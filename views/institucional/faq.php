                  <li>O aluno que indicar um amigo ou parente, terá direito a 50% (cinqüenta por cento) de 
                    desconto na última parcela do curso, desde que tenha quitado as demais.</li>
                  <li>Para que o benefício seja concedido, o aluno indicado deverá estar cursando e estar em 
                    dia com as obrigações financeiras junto ao IEFAP na data do benefício.</li>
                  <li>No caso de indicação de mais de um aluno, o desconto será aplicado na proporção de 50% 
                    (cinqüenta por cento) nas parcelas a partir da última relativas ao número de alunos 
                    indicados. Ex: No caso de indicação de 2 (dois) alunos, o aluno indicante terá desconto de 
                    100% (cem por cento) na última parcela. Se indicar 3 (três) alunos terá desconto de 100% 
                    (cem por cento) na última parcela e 50% (cinquenta por cento) na penúltima parcela. Os 
                    descontos não serão retroativos a parcelas já quitadas e sim nas parcelas a vencer.</li>
                  <li>A indicação poderá acontecer a qualquer momento, e o benefício será aplicado a partir 
                    da efetivação da matrícula pelo aluno indicado.</li>
                  <li>No caso de alunos que tiverem direito ao benefício, o IEFAP emitirá o novo boleto com 
                    no máximo 30 dias antes do vencimento, concedendo o benefício. No caso de alunos que 
                    tiverem o benefício de 100% (cem por cento) da parcela, o IEFAP efetivará a baixa e 
